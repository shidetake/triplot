-- 「旅行TZ」概念の廃止。
--
-- 経緯:
--  - 通常 / 時差移動イベントは各自 start_tz(/end_tz) を「ユーザが選んで」持つ。
--    時差移動で同じ暦日が2列（出発TZ側/到着TZ側）になったとき、通常イベントが
--    どちらの列に乗るかを決めるのに per-event TZ が必須だから。
--  - 一方、trips に単一TZを持たせる案は、米国横断のように旅程中にTZを何度も
--    跨ぐ旅行で「どれが“旅行のTZ”か」をユーザが決められず破綻する。
--  - 終日イベントのTZは表示に無関係 → アプリ層で UTC 固定にする。
--  - よって trips.time_zone は不要。普通の日の「現在TZ」は旅程（時差移動の
--    連なり）から導出する。
alter table trips drop column time_zone;
